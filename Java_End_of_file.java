import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Java_End_of_file {

    public static void main(String[] args) {
        Scanner in =new Scanner(System.in);
        int i=0;
        while(in.hasNext())
        {
            i++;
            System.out.println(i+" "+in.nextLine());
        }
        
       
    }
}

